package ua.model;

public class Adverbio {
	private String adverbio;
	private String firmeza;
	
	public Adverbio() {
		adverbio="";
		firmeza="";
	}

	public String getAdverbio() {
		return adverbio;
	}

	public void setAdverbio(String adverbio) {
		this.adverbio = adverbio;
	}

	public String getFirmeza() {
		return firmeza;
	}

	public void setFirmeza(String firmeza) {
		this.firmeza = firmeza;
	}
	
}
