package ua.model;

public class Verbo {
	private String verbo;
	private String firmeza;
	
	public Verbo() {
		setVerbo("");
		setFirmeza("");
	}

	public String getVerbo() {
		return verbo;
	}

	public void setVerbo(String verbo) {
		this.verbo = verbo;
	}

	public String getFirmeza() {
		return firmeza;
	}

	public void setFirmeza(String firmeza) {
		this.firmeza = firmeza;
	}
	
}
